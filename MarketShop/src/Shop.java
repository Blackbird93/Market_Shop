    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        customerField = cField.getText().trim();
        stockCombo = (String) sCombo.getSelectedItem();
        
        int dd = (int)cSpinner.getValue();        
        if( dd < 0 || dd > 50) {
           count = 0; 
        }
        else {
            count = dd;
        }
        
        try {
            String str = allSumField.getText();
            totalSum = Double.parseDouble(str);
        }
        catch(Exception ex) {
            totalSum = 0;
        }
        
        dds = ddsCheck.isSelected();
       
        ShopTable shop = new ShopTable(customerField, stockCombo, count, totalSum, dds);
        data.add(shop);
        showTable();
        clear();
    }//GEN-LAST:event_addBtnActionPerformed

    private void allBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_allBtnActionPerformed
        double Acount = 0;
        double Asum = 0;
        
        for( Object o : data) {
            Shop shop = (Shop) o;
            Acount += shop.totalSum + shop.count;
            if( shop.dds ) {
                Asum += (shop.totalSum * shop.count) * 0.2;
            }
        }   
    }//GEN-LAST:event_allBtnActionPerformed

