    private class MyModel extends DefaultTableModel {
        @Override
        public int getRowCount() {
            return data.size();
        }
        
        @Override
        public int getColumnCount() {
            return colNames.length;
        }
        
        @Override
        public boolean isCellEditable(int r, int c) {
            return false;
        }
        
        @Override
           public String getColumnName(int c) {
               return colNames[c];
           }   
           
        @Override
        public Object getValueAt(int r, int col) {
            if (r >= data.size()) {
                return "";
            }
            ShopTable shop = (ShopTable) data.get(r);

            switch (col) {
                case 0:
                    return shop.customer;
                case 1:
                    return shop.stock;
                case 2:
                    return shop.dds;
                case 3:
                    return shop.count;
                case 4:
                    return (shop.totalSum * shop.count) + "";
                case 5:
                    return ( (shop.price * shop.count) * 0.2) + "";
                default:
                    return "";
            }
        }   
    }
    
